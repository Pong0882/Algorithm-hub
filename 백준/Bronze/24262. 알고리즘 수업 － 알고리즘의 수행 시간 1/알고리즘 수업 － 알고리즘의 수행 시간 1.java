import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        sc.nextInt(); // 입력된 n 값은 실제 계산에 사용되지 않음
        System.out.println(1); // 코드1의 수행 횟수
        System.out.println(0); // 최고차항의 차수 (상수 시간 → 0)
    }
}
